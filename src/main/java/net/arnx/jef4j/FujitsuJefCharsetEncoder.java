	public FujitsuJefCharsetEncoder(Charset cs, SingleByteEncoding encoding) {
		super(cs, 2, 2, getReplacementChar(encoding));
		this.encoding = encoding;
	}
	
	private static byte[] getReplacementChar(SingleByteEncoding encoding) {
		if (encoding == SingleByteEncoding.NONE) {
			return new byte[] { (byte)0xA1, (byte)0xA9 };
		}
		return new byte[] { 0x6F };
	}
	
	@Override
	protected CoderResult encodeLoop(CharBuffer in, ByteBuffer out) {
		int mark = in.position();
		try {
			while (in.hasRemaining()) {
				char c = in.get();
				if (Character.isSurrogate(c)) {
					if (Character.isHighSurrogate(c)) {
						if (!in.hasRemaining()) {
							return CoderResult.UNDERFLOW;
						}
						char c2 = in.get();
						if (Character.isLowSurrogate(c2)) {
							return CoderResult.unmappableForLength(2);
						} else {
							return CoderResult.malformedForLength(1);
						}
					} else {
						return CoderResult.malformedForLength(1);
					}
				} else if (c >= '\uFFFE') {
					return CoderResult.unmappableForLength(1);
				} else if (c <= '\u007F' || c == '\u00A3' || c == '\u00A6' || c == '\u00AC') { // Single Byte
					if (encoding == SingleByteEncoding.NONE) {
						return CoderResult.unmappableForLength(1);
					}
					if (shiftin) {
						if (!out.hasRemaining()) {
							return CoderResult.OVERFLOW;
						}
						out.put((byte)0x28);
						shiftin = false;
					}
					
					byte b = 0;
					if (c <= '\u0020') {
						b = CTRL_MAP[c];
					} else {
						switch (encoding) {
						case ASCII:
							if (c <= '\u007F') {
								b = ASCII_MAP[c - 0x21];
							}
							break;
						case EBCDIC:
							if (c <= '\u007F') {
								b = EBCDIC_MAP[c - 0x21];
							} else if (c == '\u00A3') {
								b = (byte)0x4A;
							} else if (c == '\u00A6') {
								b = (byte)0x6A;
							} else if (c == '\u00AC') {
								b = (byte)0x5F;
							}
							break;
						case EBCDIK:
							if (c <= '\u007F') {
								b = EBCDIK_MAP[c - 0x21];
							} else if (c == '\u00A3') {
								b = (byte)0x4A;
							} else if (c == '\u00AC') {
								b = (byte)0x5F;
							}
							break;
						default:
							return CoderResult.unmappableForLength(1);
						}
					}
					if (b == 0 && c != '\0') {
						return CoderResult.unmappableForLength(1);
					}

					if (!out.hasRemaining()) {
						return CoderResult.OVERFLOW;
					}
					out.put((byte)b);
					mark++;
				} else if (c >= '\uFF61' && c <= '\uFF9F') { // Harfwidth katakana
					if (encoding != SingleByteEncoding.EBCDIK) {
						return CoderResult.unmappableForLength(1);
					}
					if (shiftin) {
						if (!out.hasRemaining()) {
							return CoderResult.OVERFLOW;
						}
						out.put((byte)0x28);
						shiftin = false;
					}
					
					byte b = KANA_MAP[c - 0xFF61];
					if (b == 0) {
						return CoderResult.unmappableForLength(1);
					}
					if (!out.hasRemaining()) {
						return CoderResult.OVERFLOW;
					}
					out.put(b);
					mark++;
				} else { // Double Bytes
					int n1 = (c >> 12) & 0xF;
					int n2 = (c >> 8) & 0xF;
					int n3 = (c >> 4) & 0xF;
					int n4 = c & 0xF;
					
					if (n1 < JEF_MAP.length) {
						String[][] m2 = JEF_MAP[n1];
						if (m2 != null && n2 < m2.length) {
							String[] m3 = m2[n2];
							if (m3 != null && n3 < m3.length) {
								String m4 = m3[n3];
								if (m4 != null && n4 < m4.length()) {
									char mc = m4.charAt(n4);
									if (mc != '\u0000') {
										if (encoding != SingleByteEncoding.NONE && !shiftin) {
											if (!out.hasRemaining()) {
												return CoderResult.OVERFLOW;
											}
											out.put((byte)0x29);
											shiftin = true;
										}
										
										if (out.remaining() < 2) {
											return CoderResult.OVERFLOW;
										}
										out.put((byte)((mc >> 8) & 0xFF));
										out.put((byte)(mc & 0xFF));
										mark++;
										continue;
									}
								}
							}
						}
					}
					
					return CoderResult.unmappableForLength(1);
				}
			}
		} finally {
			in.position(mark);
		}
		return CoderResult.UNDERFLOW;
	}
	
	@Override
	protected CoderResult implFlush(ByteBuffer out) {
		if (encoding != SingleByteEncoding.NONE && shiftin) {
			if (!out.hasRemaining()) {
				return CoderResult.OVERFLOW;
			}
			out.put((byte)0x28);
			shiftin = false;
		}
		return CoderResult.UNDERFLOW;
	}
	
	@Override
	protected void implReset() {
		shiftin = false;
	}
